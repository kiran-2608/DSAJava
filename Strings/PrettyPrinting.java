// package Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        
        float a = 453.1234f;
        System.out.printf("Formatted Number is %.2f\n", a);

        System.out.printf("Pie: %.3f\n" , Math.PI);

        System.out.printf("Hello my name is %s and I am %s" , "Kiran" , "Cool");

    }
}
